/**
 * 检测验证码
 * @param  integer $id 验证码ID
 * @return boolean     检测结果
 */
function check_verify($code, $id = 1){
	import('COM.ThinkVerify.ThinkVerify');
	$verify = new ThinkVerify();
	return $verify->check($code, $id);
}